package com.mycompany.ch07.third;

public class Arithemtic {
	
	//사칙연산 함수 생성(+,-,*,/,%)
	
	//method
	public static int plus(int a, int b) {
		int plus = a + b ;
		return plus;
	}
	
	public static int minus(int a, int b) {
		int minus = a - b ;
		return minus;
	}
	
	public static int multiply(int a, int b) {
		int multiply = a * b ;
		return multiply;
	}
	
	public static int divide(int a, int b) {
		int divide = a / b ;
		return divide;
	}
	
	
	
}
